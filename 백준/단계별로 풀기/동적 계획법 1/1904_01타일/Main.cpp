#include <iostream>
#include <vector>
using namespace std;

int	main(void) {
	ios::sync_with_stdio(0);
	cin.tie(0);
	int n;
	cin >> n;
	int *v = new int[n];
	v[0] = 1;
	v[1] = 2;
	for (int i=2; i<n; i++)
		v[i] = (v[i-1] + v[i-2]) % 15746;
	cout << v[n-1];
	return (0);
}

